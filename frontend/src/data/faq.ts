const FAQ_CATEGORY_ORDER = [
  'Điểm số',
  'Học lại',
  'Cảnh báo học vụ',
  'Thi cử',
  'Rèn luyện',
  'Học bổng',
  'Học phí',
  'Giấy tờ',
  'Tạm nghỉ',
  'Tốt nghiệp',
  'KTX',
  'Liên hệ',
];

function compareFaqCategory(a: string, b: string): number {
  const aIndex = FAQ_CATEGORY_ORDER.indexOf(a);
  const bIndex = FAQ_CATEGORY_ORDER.indexOf(b);
  if (aIndex === -1 && bIndex === -1) return a.localeCompare(b, 'vi');
  if (aIndex === -1) return 1;
  if (bIndex === -1) return -1;
  return aIndex - bIndex;
}

