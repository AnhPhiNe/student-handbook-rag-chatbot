export function getFaqItemsForCohort(cohort: Cohort): FaqItem[] {
  return FAQ_ITEMS
    .filter((item) => item.cohort === 'all' || item.cohort === cohort)
    .sort((a, b) => {
      const categoryOrder = compareFaqCategory(a.category, b.category);
      if (categoryOrder !== 0) return categoryOrder;
      return a.question.localeCompare(b.question, 'vi');
    });
}

export function getFaqCategoriesForCohort(cohort: Cohort): string[] {
  const categories = Array.from(new Set(getFaqItemsForCohort(cohort).map((item) => item.category)));
  return categories.sort(compareFaqCategory);
}
